/**
 ******************************************************************************
 * @brief   输出os logo
 * @param[in]  None
 * @param[out] None
 *
 * @retval     None
 ******************************************************************************
 */
void
os_print_logo(void)
{
    printf("\r\n"
           " \\|/\n"
           "--O-- The CM3 RTOS is starting...\n"
           " /|\\\n");
}

/**
 ******************************************************************************
 * @brief   输出os版本
 * @param[in]  None
 * @param[out] None
 *
 * @retval     None
 ******************************************************************************
 */
void
os_print_banner(void)
{
    printf("\n  OS Version: "OS_RELEASE
           "\n  FreeRTOS  : V7.3.0"
           "\n  Build Time: "__DATE__" "__TIME__
           "\n  CopyRight : Xxxx Xxxx..\n");
}

extern status_t
devnull_create(void);
/**
 ******************************************************************************
 * @brief   os资源初始化
 * @param[in]  None
 * @param[out] None
 *
 * @retval     None
 ******************************************************************************
 */
status_t
os_resource_init(void)
{
    (void)devlib_init();
    (void)devnull_create();

    return OK;
}
